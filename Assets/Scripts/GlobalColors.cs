	public static Color32[] GetPressedAndLightColor(Color32 sendColor, GameObject gameObject)
	{
		if (CompareColor(sendColor, PRIMARY_ACTION_COLOR))
		{
			return new Color32[4]
			{
				PRIMARY_ACTION_COLOR_LIGHT,
				PRIMARY_ACTION_COLOR_SELECTED,
				PRIMARY_ACTION_COLOR_UNAVAILABLE,
				PRIMARY_ACTION_COLOR
			};
		}
		if (CompareColor(sendColor, TERTIARY_ACTION_COLOR))
		{
			return new Color32[4]
			{
				TERTIARY_ACTION_COLOR_LIGHT,
				TERTIARY_ACTION_COLOR_SELECTED,
				TERTIARY_ACTION_COLOR_UNAVAILABLE,
				TERTIARY_ACTION_COLOR
			};
		}
		if (CompareColor(sendColor, FOOTER_ME_COLOR))
		{
			return new Color32[2]
			{
				FOOTER_ME_COLOR_LIGHT,
				FOOTER_ME_COLOR_SELECTED
			};
		}
		if (CompareColor(sendColor, FOOTER_SHOP_COLOR))
		{
			return new Color32[2]
			{
				FOOTER_SHOP_COLOR_LIGHT,
				FOOTER_SHOP_COLOR_SELECTED
			};
		}
		if (CompareColor(sendColor, FOOTER_SOCIAL_COLOR))
		{
			return new Color32[2]
			{
				FOOTER_SOCIAL_COLOR_LIGHT,
				FOOTER_SOCIAL_COLOR_SELECTED
			};
		}
		if (CompareColor(sendColor, FOOTER_BACK_COLOR))
		{
			return new Color32[1]
			{
				FOOTER_BACK_COLOR_LIGHT
			};
		}
		if (CompareColor(sendColor, FOOTER_BIG_PLAY_RESUME_COLOR))
		{
			return new Color32[1]
			{
				FOOTER_BIG_PLAY_RESUME_COLOR_LIGHT
			};
		}
		if (CompareColor(sendColor, FOOTER_BIG_WAIT_COLOR))
		{
			return new Color32[4]
			{
				FOOTER_BIG_WAIT_COLOR,
				FOOTER_BIG_WAIT_COLOR,
				FOOTER_BIG_WAIT_COLOR,
				FOOTER_BIG_WAIT_COLOR
			};
		}
		if (CompareColor(sendColor, SOCIAL_GREY))
		{
			return new Color32[4]
			{
				SOCIAL_GRAY_OVERLAY,
				FOOTER_BIG_WAIT_COLOR,
				FOOTER_BIG_WAIT_COLOR,
				FOOTER_BIG_WAIT_COLOR
			};
		}
		if (CompareColor(sendColor, SECONDARY_ACTION_COLOR))
		{
			return new Color32[1]
			{
				SECONDARY_ACTION_COLOR_LIGHT
			};
		}
		if (CompareColor(sendColor, FACEBOOK_COLOR))
		{
			return new Color32[1]
			{
				FACEBOOK_COLOR_LIGHT
			};
		}
		if (CompareColor(sendColor, GAMECENTER_COLOR))
		{
			return new Color32[1]
			{
				GAMECENTER_COLOR_LIGHT
			};
		}
		if (CompareColor(sendColor, SEASONAL_COLOR_FILL_NORMAL))
		{
			return new Color32[1]
			{
				SEASONAL_COLOR_FILL_LIGHT
			};
		}
		return null;
	}

	private static bool CompareColor(Color32 a, Color32 b)
	{
		return a.Equals(b);
	}
